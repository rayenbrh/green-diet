const tagPool = [
  'Sans Gluten',
  'Bio',
  'Vegan',
  'Sans Lactose',
  'Artisanal',
  'Riche en fibres',
  'Naturel',
  'Fait maison',
]

function pickTags(seed) {
  const count = 2 + (seed % 3)
  const out = []
  let i = seed
  while (out.length < count) {
    out.push(tagPool[i % tagPool.length])
    i += 3
  }
  return [...new Set(out)]
}

